    private void btnSendMailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendMailActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String emailID = txtEmail.getText();
        String msg = txtMsg.getText();
        String phoneno = txtPhone.getText();

        if (name.equals("") && emailID.equals("") && msg.equals("") && phoneno.equals("")) {
            JOptionPane.showMessageDialog(null, "Please provide all details", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            sendEmail(name, emailID, msg);
            CommunityOutreach co = business.addDatatoCommunityOutreach();
            co.setName(name);
            co.setEmail_id(emailID);
            co.setMessage(msg);
            co.setPhone_no(phoneno);
            populateTableData();
        }
    }//GEN-LAST:event_btnSendMailActionPerformed

    private void populateTableData(){
        DefaultTableModel model = (DefaultTableModel) tblHistory.getModel();
        model.setRowCount(0);
        
        for(CommunityOutreach co: business.getCoomunityOutreachData()){
           Object[] rowData = new Object[4];
           rowData[0] = co.getName();
           rowData[1] = co.getEmail_id();
           rowData[2] = co.getMessage();
           rowData[3] = co.getPhone_no();
           model.addRow(rowData);
        }
    }
    
    
