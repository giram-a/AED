    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDonationInfo.getModel();
        model.setRowCount(0);
        for (WorkRequest w : account.getWorkQueue().getWorkRequestList()) {
            if (w instanceof DonationRequest) {
                Object row[] = new Object[5];
                DonationRequest dreq = (DonationRequest) w;
                if(!dreq.getStatus().equals("Approved")){
                    row[0] = dreq;
                    row[1] = dreq.getSender().getPerson().getName();
                    row[2] = dreq.getMessage();
                    row[3] = dreq.getReceiver();
                    row[4] = dreq.getStatus();
                    model.addRow(row);
                }
            }
        }

        if ((model.getRowCount() == 0)) {
            JOptionPane.showMessageDialog(null, "No Donation request found", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }

