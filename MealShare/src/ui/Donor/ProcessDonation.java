    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDonationInfo.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row", "warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DonationRequest req = (DonationRequest) tblDonationInfo.getValueAt(selectedRow, 0);
        req.setReceiver(account);
        req.setStatus("In Process");
        populateTable();
        JOptionPane.showMessageDialog(this, "Request Assigned to you, you can now process it", "Information", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnProcessDonationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessDonationActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDonationInfo.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row", "warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DonationRequest req = (DonationRequest) tblDonationInfo.getValueAt(selectedRow, 0);
        if (req.getReceiver() != null) {
            DonateMoneyJPanel donateMoneyJPanel = new DonateMoneyJPanel(userProcessContainer, account, enterprise, organization, business, req);
            business.redirection(userProcessContainer, donateMoneyJPanel.getClass().getName(), donateMoneyJPanel);
        } else {
            JOptionPane.showMessageDialog(this, "Please Assign the request first", "warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnProcessDonationActionPerformed


