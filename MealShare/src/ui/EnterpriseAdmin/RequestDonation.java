    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        // TODO add your handling code here:
        int index = cbDonor.getSelectedIndex();
        String comment = txtComments.getText();
        if(comment.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter comment");
            return;
        }
        
        DonationRequest donationRequest = new DonationRequest();
        donationRequest.setSender(account);
        donationRequest.setRequestDate(new Date());
        donationRequest.setMessage(comment);
        donationRequest.setStatus("Sent");
        donationRequest.setRequestDate(new Date());
        
        UserAccount user = users.get(index);
        System.out.println("Requesting donation to usr " + user);
        System.out.println("User " + user.getPerson().getName());
        user.getWorkQueue().getWorkRequestList().add(donationRequest);
        account.getWorkQueue().getWorkRequestList().add(donationRequest);
        JOptionPane.showMessageDialog(this, "Donation request sent", "Information", JOptionPane.INFORMATION_MESSAGE);
        txtComments.setText("");
    }//GEN-LAST:event_btnRequestActionPerformed


