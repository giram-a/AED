    private void populateCombo() {
        cbDonor.removeAllItems();
        int index = 1;
        cbDonor.addItem("Please select a donor");
        for (Organization e : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : e.getUserAccountDirectory().getUserAccountList()) {
                cbDonor.addItem(ua.getPerson().getName());
                users.put(index++, ua);
            }
        }
    }

